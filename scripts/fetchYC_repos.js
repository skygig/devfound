const fetchRealRepos = async () => {
    for (ycBatch of Object.values(allYC_Companies)) {
        const list = ycBatch.list;
        let currBatch = 2025;

        console.log("============ Batch : ", currBatch, " ====================")
        await fs.appendFile('example.txt', `============ Batch : ", ${currBatch}, " ====================\n`, 'utf8');
        currBatch -= 1;
        let count = 0;

        for (startup of list) {
            const split = startup.split("/");
            if (split.length > 4) {
                await fs.appendFile('example.txt', `"${startup}",\n`, 'utf8');
                count += 1;
                continue;
            };

            const org = split[3];
            try {
                const repos = await fetch(`https://api.github.com/users/${org}/repos`, {
                    headers: {
                        'Authorization': `token ${GITHUB_TOKEN}`,
                        'User-Agent': 'Node.js App'
                    }
                });
                if (!repos.ok) {
                    console.warn(`Skipping ${startup}: ${repos.status} ${repos.statusText}`);
                    continue;
                }

                const data = await repos.json();

                let currMaxStar = 0;
                let popularRepo = "";
                for (repo of data) {
                    if (repo.forks_count === 0) continue;

                    if (repo.stargazers_count > currMaxStar) {
                        currMaxStar = repo.stargazers_count;
                        popularRepo = repo.html_url
                        count += 1
                    }
                }

                if (popularRepo) {
                    await fs.appendFile('example.txt', `'${popularRepo}',\n`, 'utf8');
                }
            } catch (e) {
                console.log("============ API didn't worked for :", startup);
                console.error(e)
            }
        }

        console.log(`Found ${count} actual repos out of ${list.length}`);
        await fs.appendFile('example.txt', `Found ${count} actual repos out of ${list.length}\n\n`, 'utf8');
        count = 0;
    }
}

