const fs = require('fs/promises');

const GITHUB_TOKEN = process.env.GITHUB_TOKEN;

const fetchRepoDetails = async () => {
    let currBatch = 2025;

    for (reposList of Object.values(all_Repos)) {

        console.log("============ Batch : ", currBatch, " ====================")
        await fs.appendFile('finalData.txt', `============ Batch : ", ${currBatch}, " ====================\n`, 'utf8');
        currBatch -= 1;

        for (currRepo of reposList) {
            try {
                const split = currRepo.split("/")
                const response = await fetch(`https://api.github.com/repos/${split[3]}/${split[4]}`, {
                    headers: {
                        'Authorization': `token ${GITHUB_TOKEN}`,
                        'User-Agent': 'Node.js App'
                    }
                });
                if (!response.ok) {
                    console.warn(`Skipping ${currRepo}: ${response.status} ${response.statusText}`);
                    continue;
                }

                const languagesRes = await fetch(`https://api.github.com/repos/${split[3]}/${split[4]}/languages`, {
                    headers: {
                        'Authorization': `token ${GITHUB_TOKEN}`,
                        'User-Agent': 'Node.js App'
                    }
                });
                if (!languagesRes.ok) {
                    console.warn(`Skipping ${currRepo}: ${languagesRes.status} ${languagesRes.statusText}`);
                    continue;
                }

                const issuesRes = await fetch(`https://api.github.com/repos/${split[3]}/${split[4]}/issues`, {
                    headers: {
                        'Authorization': `token ${GITHUB_TOKEN}`,
                        'User-Agent': 'Node.js App'
                    }
                });
                if (!issuesRes.ok) {
                    console.warn(`Skipping ${currRepo}: ${issuesRes.status} ${issuesRes.statusText}`);
                    continue;
                }

                const issues = await issuesRes.json();
                let issuesCount = 0;
                let goodFirstIssuesCount = 0;

                for (issue of issues) {
                    if (issue.state != "open") continue
                    issuesCount += 1;

                    if (issue.labels.length == 0) continue;
                    for (label of issue.labels) {
                        if (label.name.toLowerCase().includes("good")) {
                            goodFirstIssuesCount += 1;
                            break;
                        }
                    }
                }

                const langData = await languagesRes.json();
                const data = await response.json();

                const requiredData = {
                    title: data.name,
                    desc: data.description,
                    avatar: data.owner.avatar_url,
                    languages: langData,
                    forks: data.forks_count,
                    stars: data.stargazers_count,
                    issues: issuesCount,
                    goodFirstIssues: goodFirstIssuesCount
                }
                await fs.appendFile('finalData.txt', `${JSON.stringify(requiredData)},\n`, 'utf8');
            } catch (e) {
                console.log("============ API didn't worked for :", currRepo);
                console.error(e)
            }
        }

        await fs.appendFile('finalData.txt', `===================================================\n\n`, 'utf8');
    }
}

